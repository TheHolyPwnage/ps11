CREATE TABLE category(
	id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	title VARCHAR(30),
	author VARCHAR(20),
	description VARCHAR(150),
	active BOOLEAN
);

CREATE TABLE post(
	id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	categoryId INTEGER CONSTRAINT fk_category REFERENCES category(id) ON DELETE CASCADE ON UPDATE RESTRICT,
	author VARCHAR(20),
	content VARCHAR(255)
);